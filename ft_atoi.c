#include "libft.h"

int	ft_atoi(const char *str)
{
	int	c;
	int	s;
	long long	res;

	c = 0;
	s = 1;
	res = 0;
	while ((str[c] >= '\t' && str[c] <= '\r') || str[c] == ' ')
		c++;
	while (str[c] == '+' || str[c] == '-')
	{
		if (str[c] == '-')
			s *= -1;
		c++;
	}
	while (str[c] >= '0' && str[c] <= '9')
	{
		if (res * s > 2147483647)
			return (-1);
		else if (res * s < -2147483648)
			return (0);
		else
			res = (str[c] - '0') + (res * 10);
		c++;
	}
	return (res * s);
}
